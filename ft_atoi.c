#include	"libft.h"

int	ft_atoi(const char *str)
{
	int			i;
	int			s;
	size_t		r;

	i = 0;
	r = 0;
	s = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{		
		r = r * 10 + (str[i] - '0');
		if (r > 9223372036854775807 && s == -1)
			return (0);
		if (r > 9223372036854775807 && s == 1)
			return (-1);
		i++;
	}
	return (r * s);
}
