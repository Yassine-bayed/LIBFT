#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (void *)malloc(sizeof(t_list));
	if (!new)
		return (NULL);
	new->content = content;
	new->next = NULL;
	return (new);
}

int	main(void)
{
	t_list	*new;
	int		*i;
	int		a;

	a = 5;
	i = &a;
	new = ft_lstnew(i);
	printf("%p", (new->content));
	return (0);
}